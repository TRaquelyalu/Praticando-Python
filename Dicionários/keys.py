'''
O método keys() retorna todas as chaves do dicionário como uma coleção (um objeto do tipo dict_keys).
'''
# Exibindo todas as chaves do dicionário


print(estudante.keys())  # Saída: dict_keys(['nome', 'idade', 'curso'])

# Convertendo as chaves para uma lista
print(list(estudante.keys()))  # Saída: ['nome', 'idade', 'curso']