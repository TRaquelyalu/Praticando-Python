'''
As tuplas são coleções de itens imutáveis (você não pode alterar, adicionar ou remover elementos) e permitem itens duplicados.

Características das tuplas:
Definidas com parênteses ().
Mais rápidas e seguras que listas quando os dados não precisam ser alterados.
'''
#Exemplo: Criando uma tupla
cores = ("vermelho", "azul", "verde")
print(cores)

#Acessando elementos de uma tupla
#Funciona como nas listas.

print(cores[0])  # Primeiro item: vermelho
print(cores[-1]) # Último item: verde