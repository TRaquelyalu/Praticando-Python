# Convertendo um número para string para exibição
quantidade = 5
mensagem = "Você tem " + str(quantidade) + " itens no carrinho."
print(mensagem)

'''
A função str() transforma qualquer valor em texto (string). Ela é útil quando precisamos exibir um número ou outro valor junto com texto.

Aqui, str(quantidade) converte o número quantidade para texto, permitindo que ele seja concatenado à mensagem.
'''