# Convertendo uma string para um número inteiro
idade = int(input("Digite sua idade: "))
print("Daqui a 5 anos, você terá", idade + 5, "anos.")

'''
A função int() converte um valor para um número inteiro. É útil, por exemplo, para transformar dados de entrada (que vêm como texto) em números para cálculos.

Aqui, int() converte a entrada do usuário para um inteiro, permitindo realizar a operação idade + 5.

'''