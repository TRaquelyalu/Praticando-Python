'''O método values() retorna todos os valores armazenados no dicionário, também como uma coleção (dict_values).
'''
# Exibindo todos os valores do dicionário
print(estudante.values())  # Saída: dict_values(['Ana', 20, 'Matemática'])

# Convertendo os valores para uma lista
print(list(estudante.values()))  # Saída: ['Ana', 20, 'Matemática']


