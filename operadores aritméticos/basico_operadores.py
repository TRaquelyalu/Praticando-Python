a = 10
b = 5
c = 3

# Operadores Aritméticos
print("Adição:", a + b)
print("Divisão Inteira:", a // c)

# Operadores de Comparação
print("a é maior que b?", a > b)
print("c é igual a 3?", c == 3)

# Operadores Lógicos
print("a é maior que b e c é menor que b?", (a > b) and (c < b))
print("a é menor que b ou c é menor que a?", (a < b) or (c < a))